-- 1. SP_INS_PUBLIC_ENCRYPT_NHANVIEN: Thêm nhân viên mới (Mã hóa và băm được thực hiện ở Client)
CREATE OR ALTER PROCEDURE SP_INS_PUBLIC_ENCRYPT_NHANVIEN
    @MANV VARCHAR(20),
    @HOTEN NVARCHAR(100),
    @EMAIL VARCHAR(20),
    @LUONG VARBINARY(MAX),
    @TENDN NVARCHAR(100),
    @MK VARBINARY(MAX),
    @PUB VARCHAR(MAX)
AS
BEGIN
    SET NOCOUNT ON;
    BEGIN TRANSACTION;
    BEGIN TRY
        -- Kiểm tra trùng mã nhân viên
        IF EXISTS (SELECT 1 FROM NHANVIEN WHERE MANV = @MANV)
            THROW 50001, N'Mã nhân viên đã tồn tại.', 1;

        -- Kiểm tra trùng tên đăng nhập
        IF EXISTS (SELECT 1 FROM NHANVIEN WHERE TENDN = @TENDN)
            THROW 50002, N'Tên đăng nhập đã tồn tại.', 1;

        -- Lưu trực tiếp dữ liệu đã mã hóa và băm từ client
        INSERT INTO NHANVIEN (MANV, HOTEN, EMAIL, LUONG, TENDN, MATKHAU, PUBKEY)
        VALUES (@MANV, @HOTEN, @EMAIL, @LUONG, @TENDN, @MK, @PUB);

        COMMIT TRANSACTION;
    END TRY
    BEGIN CATCH
        IF @@TRANCOUNT > 0 ROLLBACK;
        DECLARE @ErrMsg NVARCHAR(4000) = ERROR_MESSAGE();
        RAISERROR(@ErrMsg, 16, 1);
    END CATCH
END
GO

-- 2. SP_SEL_PUBLIC_ENCRYPT_NHANVIEN: Lấy thông tin nhân viên kèm lương đã mã hóa
CREATE OR ALTER PROCEDURE SP_SEL_PUBLIC_ENCRYPT_NHANVIEN
    @TENDN NVARCHAR(100),
    @MK VARBINARY(MAX)
AS
BEGIN
    SET NOCOUNT ON;
    SELECT
        n.MANV,
        n.HOTEN,
        n.EMAIL,
        n.LUONG,
        n.PUBKEY,
        n.TENDN
    FROM NHANVIEN n
    WHERE n.TENDN = @TENDN COLLATE SQL_Latin1_General_CP1_CS_AS
      AND n.MATKHAU = @MK;
END
GO


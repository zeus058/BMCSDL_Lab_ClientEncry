-- 13. SP_UPD_SINHVIEN: Cập nhật thông tin sinh viên có phân quyền sở hữu
CREATE OR ALTER PROCEDURE SP_UPD_SINHVIEN
    @CALLER_MANV VARCHAR(20),
    @MASV VARCHAR(20),
    @HOTEN NVARCHAR(100),
    @NGAYSINH DATETIME,
    @DIACHI NVARCHAR(200),
    @TENDN NVARCHAR(100),
    @MK VARBINARY(MAX) = NULL
AS
BEGIN
    SET NOCOUNT ON;
    -- Ràng buộc: Giáo viên phải quản lý lớp của sinh viên này mới được phép cập nhật
    IF NOT EXISTS (
        SELECT 1 FROM SINHVIEN s
        INNER JOIN LOP l ON l.MALOP = s.MALOP
        WHERE s.MASV = @MASV AND l.MANV = @CALLER_MANV
    )
        THROW 50009, N'Bạn không có quyền cập nhật thông tin sinh viên này.', 1;

    -- Kiểm tra trùng tên đăng nhập của sinh viên khác
    IF EXISTS (SELECT 1 FROM SINHVIEN WHERE TENDN = @TENDN AND MASV <> @MASV)
        THROW 50002, N'Tên đăng nhập đã tồn tại.', 1;

    UPDATE SINHVIEN
    SET HOTEN = @HOTEN,
        NGAYSINH = @NGAYSINH,
        DIACHI = @DIACHI,
        TENDN = @TENDN,
        MATKHAU = COALESCE(@MK, MATKHAU)
    WHERE MASV = @MASV;
END
GO

-- 14. SP_DEL_SINHVIEN_BY_OWNER: Xóa sinh viên và bảng điểm liên quan
CREATE OR ALTER PROCEDURE SP_DEL_SINHVIEN_BY_OWNER
    @CALLER_MANV VARCHAR(20),
    @MASV VARCHAR(20)
AS
BEGIN
    SET NOCOUNT ON;
    IF NOT EXISTS (
        SELECT 1 FROM SINHVIEN s
        INNER JOIN LOP l ON l.MALOP = s.MALOP
        WHERE s.MASV = @MASV AND l.MANV = @CALLER_MANV
    )
        THROW 50011, N'Không có quyền xóa sinh viên này.', 1;

    BEGIN TRANSACTION;
    BEGIN TRY
        DELETE FROM BANGDIEM WHERE MASV = @MASV;
        DELETE FROM SINHVIEN WHERE MASV = @MASV;
        COMMIT;
    END TRY
    BEGIN CATCH
        ROLLBACK;
        THROW;
    END CATCH
END
GO

-- 15. SP_UPSERT_BANGDIEM: Cập nhật hoặc thêm mới điểm (Đã mã hóa từ Client)
CREATE OR ALTER PROCEDURE SP_UPSERT_BANGDIEM
    @CALLER_MANV VARCHAR(20),
    @MASV VARCHAR(20),
    @MAHP VARCHAR(20),
    @DIEMTHI VARBINARY(MAX)
AS
BEGIN
    SET NOCOUNT ON;
    -- Kiểm tra quyền quản lý sinh viên trước khi nhập điểm
    IF NOT EXISTS (
        SELECT 1 FROM SINHVIEN s
        INNER JOIN LOP l ON l.MALOP = s.MALOP
        WHERE s.MASV = @MASV AND l.MANV = @CALLER_MANV
    )
        THROW 50013, N'Không có quyền nhập điểm cho sinh viên này.', 1;

    IF EXISTS (SELECT 1 FROM BANGDIEM WHERE MASV = @MASV AND MAHP = @MAHP)
        UPDATE BANGDIEM SET DIEMTHI = @DIEMTHI WHERE MASV = @MASV AND MAHP = @MAHP;
    ELSE
        INSERT INTO BANGDIEM (MASV, MAHP, DIEMTHI) VALUES (@MASV, @MAHP, @DIEMTHI);
END
GO

-- 16. SP_SEL_BANGDIEM_DETAILED_BY_STUDENT: Xem bảng điểm chi tiết của sinh viên
CREATE OR ALTER PROCEDURE SP_SEL_BANGDIEM_DETAILED_BY_STUDENT
    @CALLER_MANV VARCHAR(20),
    @MASV VARCHAR(20)
AS
BEGIN
    SET NOCOUNT ON;
    SELECT b.MASV, b.MAHP, h.TENHP, h.SOTC, b.DIEMTHI
    FROM BANGDIEM b
    INNER JOIN HOCPHAN h ON h.MAHP = b.MAHP
    INNER JOIN SINHVIEN s ON s.MASV = b.MASV
    INNER JOIN LOP l ON l.MALOP = s.MALOP
    WHERE l.MANV = @CALLER_MANV AND b.MASV = @MASV
    ORDER BY b.MAHP;
END
GO

